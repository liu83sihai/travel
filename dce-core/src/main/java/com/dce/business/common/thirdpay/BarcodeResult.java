package com.dce.business.common.thirdpay;

public class BarcodeResult {
	
	private String app_id;
	private String inst_amount;
	private String nonce_str;
	private String extpackage;
	private String pay_sign;
	private String prepay_id;
	private String time_stamp;
	private String partner_id;
	public String getApp_id() {
		return app_id;
	}
	public void setApp_id(String app_id) {
		this.app_id = app_id;
	}
	public String getInst_amount() {
		return inst_amount;
	}
	public void setInst_amount(String inst_amount) {
		this.inst_amount = inst_amount;
	}
	public String getNonce_str() {
		return nonce_str;
	}
	public void setNonce_str(String nonce_str) {
		this.nonce_str = nonce_str;
	}
	public String getExtpackage() {
		return extpackage;
	}
	public void setExtpackage(String extpackage) {
		this.extpackage = extpackage;
	}
	public String getPay_sign() {
		return pay_sign;
	}
	public void setPay_sign(String pay_sign) {
		this.pay_sign = pay_sign;
	}
	public String getPrepay_id() {
		return prepay_id;
	}
	public void setPrepay_id(String prepay_id) {
		this.prepay_id = prepay_id;
	}
	public String getTime_stamp() {
		return time_stamp;
	}
	public void setTime_stamp(String time_stamp) {
		this.time_stamp = time_stamp;
	}
	public String getPartner_id() {
		return partner_id;
	}
	public void setPartner_id(String partner_id) {
		this.partner_id = partner_id;
	}
	
	
	

}
